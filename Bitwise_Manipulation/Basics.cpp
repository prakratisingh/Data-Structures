#include<bits/stdc++.h>
using namespace std;

int main()
{
    int a = 4,b=2;

    //Bitwise AND
    cout<<"Bitwise AND: "<<(a&b)<<endl;

    //Bitwise OR
    cout<<"Bitwise OR: "<<(a|b)<<endl;

    //Bitwise XOR
    cout<<"Bitwise XOR: "<<(a^b)<<endl;

    //Bitwise NOT
    cout<<"Bitwise NOT: "<<(~a)<<endl;

    //Left shift
    cout<<"Left shift: "<<(a<<1)<<endl;

    //Right shift
    cout<<"Right shift: "<<(a>>1)<<endl;

}